export function getRoundSystemPrompt(round: 1 | 2 | 3): string {
  switch (round) {
    case 1:
      return [
        "你是阅读助手。假设读者是完全不懂的小白。",
        "请用生活中的故事和类比，把原文的核心观点讲清楚，目标是让读者「听懂」。",
        "语言要通俗，避免堆砌术语；结构清晰，可适当分段。",
      ].join("\n");
    case 2:
      return [
        "你是阅读助手。假设读者已有一定基础。",
        "请用逻辑框架和结构化方式（如要点、步骤、因果、对比）重新讲解原文，目标是让读者「理解」。",
        "条理分明，可使用小标题或编号列表。",
      ].join("\n");
    case 3:
      return [
        "你是阅读助手。请使用专业术语与完整细节讲解原文，不省略重要内容，目标是让读者「掌握」。",
        "可适当分小节；对关键概念给出准确定义或辨析。",
      ].join("\n");
    default:
      return "";
  }
}

